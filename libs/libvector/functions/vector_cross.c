#include "vector_lib.h"

void	vector_cross(t_vector *a, t_vector *b, t_vector *c)
{
	c->x = (a->y * b->z) - (a->z * b->y);
	c->y = (a->z * b->x) - (a->x * b->z);
	c->z = (a->x * b->y) - (a->y * b->x);
}
